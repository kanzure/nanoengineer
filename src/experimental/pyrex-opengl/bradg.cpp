template <class T>
T minimum(T v1, T v2)
{
    return (v1 < v2) ? v1 : v2;
}

template <class T>
T clamp(T mini, T v, T maxi)
{
    return (v < mini) ? mini : ((v > maxi) ? maxi : v);
}

